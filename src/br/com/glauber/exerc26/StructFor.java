package br.com.glauber.exerc26;

public class StructFor {
    public static void main(String[] args) {

        for (var i = 0; i < 10; i++) {
            System.out.println("WHAT IS JAVA MATRIX?");
        }
        System.out.println();
        for (var i = 10; i > 0; i--) {
            System.out.println("posicao " + i);
            System.out.println("WHAT IS JAVA MATRIX?");
        }
    }
}
