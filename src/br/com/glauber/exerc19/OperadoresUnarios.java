package br.com.glauber.exerc19;

public class OperadoresUnarios {
    public static void main(String[] args) {

        var numero = 100;

        numero++;
        System.out.println(numero++);
        System.out.println(numero);

        numero--;
        System.out.println(++numero);
        System.out.println(numero);

        ++numero;
        System.out.println(numero);

        --numero;
        System.out.println(numero);
    }
}

